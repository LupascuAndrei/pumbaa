import utime as time

import board
import pin

# Initialize the LED pin and set it high.
LED = pin.Pin(board.PIN_LED, pin.OUTPUT)
LED.write(1)

# Toggle the LED state periodically.
while True:
    time.sleep(0.5)
    LED.toggle()
